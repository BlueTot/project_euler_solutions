from math import sqrt

def prime_factorisation(num):
    factors = {}
    while num % 2 == 0:
        num //= 2
        if 2 not in factors:
            factors[2] = 1
        else:
            factors[2] += 1
    for factor in range(3, int(sqrt(num))+1, 2):
        while num % factor == 0:
            if factor not in factors:
                factors[factor] = 1
            else:
                factors[factor] += 1
            num //= factor
    if num > 2:
        factors[num] = 1
    return factors

